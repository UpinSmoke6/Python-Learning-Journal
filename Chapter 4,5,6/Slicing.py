friends = ["Rolf", "Charlie", "Anna", "Bob", "Jen"]

print(friends[1:]) # all but the first
print(friends[:]) #new list
print(friends[:4]) # all 5 in list
print(friends[-3:]) # from the end of list
print(friends[:-2]) # all but last 2
print(friends[-3:-2]) # start at 3 in from the right and take the last 2 away.